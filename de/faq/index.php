                    <h3>Was ist Mageia?</h3>
                    <p>Mageia ist Deine neue Linux-Distribution.</p>
                    
                    <h3>Wie spricht man Mageia aus?</h3>
                    <p>Es gibt keine offizielle Ausspracheregelung, daher kannst Du es aussprechen wie Du magst.</p>
                    
                    <h3>Was bedeutet der Name "Mageia"?</h3>
                    <p>"Mageia" — μαγεία in griechisch — bedeutet "Magie".</p>
                    
                    <h3>Ich benutze Mandriva. Was sollte ich jetzt tun?</h3>
                    <p>Da es noch kein Release von Mageia gibt, kannst Du erstmal Dein Mandriva weiterbenutzen.
                        Du kannst Dich beim 
                        <a href="https://www.mageia.org/mailman/listinfo/mageia-announce" hreflang="en">
                            mageia-announce Newsletter</a> anmelden um informiert zu werden, 
                        wenn das erste Release von Mageia fertig ist.</p>
                    
                    <h3>Werde ich von Mandriva zu Mageia updaten können?</h3>
                    <p>Ja, das wird möglich sein.</p>
                    
                    <h3>Wann wird das erste Release von Mageia fertig sein?</h3>
                    <p>So schnell wie möglich. Wir hoffen, dass das erste Alpha- oder Beta-Release Ende November fertig sein wird.</p>

                    <h3>Was wird der Codename des ersten Release sein?</h3>
                    <p>Darüber wurde noch nicht entschieden.</p>
                    
                    <h3>Werden urpmi, MCC, drakxtools in Mageia enthalten sein?</h3>
                    <p>Ja, Deine Lieblingstools werden auch in Mageia integriert sein.</p>

                    <h3>Wo kann ich meine Ideen für ein Logo einbringen?</h3>
                    <p>Du kannst Deine Vorschläge in der <a href="http://www.flickr.com/groups/mageia-art/" hreflang="en">Mageia art group</a> einreichen.</p>

                    <h3>Wie kann ich etwas zu Mageia beitragen?</h3>
                    <p>Es werden Leute mit allen möglichen Fähigkeiten benötigt.
                        Du kannst Dich <a href="http://mageia.org/wiki/doku.php">auf unserem Wiki</a>
                        für ein Team registrieren, das am ehesten Deinen Fähigkeiten entspricht.</p>

                    <h3>Wie kann ich mit dem Projekt in Kontakt treten?</h3>
                    <p>Du kannst eine eMail an mageia-contact (at) mageia (dot) org schicken.</p>

                    <h3>Wie kann ich das Projekt finanziell unterstützen?</h3>
                    <p>Informationen dazu kannst Du auf unserer <a href="http://donate.mageia.org/">Spendenseite</a> finden.</p>
