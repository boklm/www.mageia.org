                                <a href="/wiki/doku.php?id=triage">Grow the Triage team</a> and be the link between users who report bugs
                                in the forums or mailing-lists, and <a href="http://bugs.mageia.org/">Mageia Bugzilla</a>
                                used by developers.</p>
