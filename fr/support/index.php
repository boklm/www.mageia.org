                    <h2 id="pro">Assistance professionnelle</h2>
                    <p>L&rsquo;association Mageia.Org ne propose pas de service commercial (assistance, formation, autre)
                        autour de la distribution&nbsp;; ni aux particuliers, ni aux professionnels.
                        Par contre, des sociétés peuvent fournir de tels services.
