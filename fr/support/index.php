                    <h2 id="hw">Compatibilité matérielle</h2>
                    <p>Mageia fonctionne avec la plupart des ordinateurs x86 (32 et 64 bits) disponibles à ce jour (juin 2011).</p>

                    <p>You can follow this hardware requirements list:</p>
                    <ul class="hl">
                        <li>Processeur&nbsp;: n&rsquo;importe quel modèle AMD, Intel ou VIA&nbsp;;</li>
                        <li>Mémoire vive&nbsp;: 512&nbsp;Mo minimum, 2&nbsp;Go recommandé&nbsp;;</li>
                        <li>Disque dur&nbsp;: 1&nbsp;Go pour une installation a minima, 6&nbsp;Go pour une complète&nbsp;;</li>
                        <li>Lecteur optique&nbsp;: CD ou DVD selon l&rsquo;ISO que vous choisissez 
                            (vous pouvez aussi installer via le réseau ou avec une clé USB);</li>
                        <li>Carte vidéo&nbsp;: n&rsquo;importe ATI, Intel, Matrox, nVidia, SiS ou VIA&nbsp;;</li>
                        <li>Carte son&nbsp;: n&rsquo;importe AC97, HDA ou Sound Blaster.</li>
                    </ul>
                    <p>Certains composants (cartes Wi-Fi, cartes vidéo 3D) nécessitent
                        des pilotes logiciels spécifiques, non-libres, pour fonctionner correctement&nbsp;;
                        ils sont disponibles dans une librairie logicielle nommée "nonfree".</p>

                    <p class="dlinfo">Nous allons peut-être monter une base de compatibilité 
                        et certification matérielles, ainsi qu&rsquo;un process de certification.
                        Mais ça n&rsquo;est encore qu&rsquo;un projet &ndash;
                        si cela vous intéresse et que vous pensez pouvoir y contribuer,
                        n&rsquo;hésitez pas à contacter l&rsquo;équipe <a href="/wiki/doku.php?id=qateam">QA</a>.</p>
